package TA04;

public class Ex3App {

	public static void main(String[] args) {
		int X = 3;
		int Y = 8;
		
		double N = 1.45; 
		double M = 9.77;
		
		//Valors variables
		System.out.println("Variable X = "+X);
		System.out.println("Variable Y = "+Y);
		System.out.println("Variable N = "+N);
		System.out.println("Variable M = "+M);
		
		//Operacions
		System.out.println("Suma X+Y = " +(X+Y));
		System.out.println("Diferencia X-Y = " +(X-Y));
		System.out.println("Producte X*Y = " +(X*Y));
		System.out.println("Quocient X/Y = " +(X/Y));
		System.out.println("Resto X%Y = " +(X%Y));
		
		System.out.println("Suma N+M = " +(N+M));
		System.out.println("Diferencia N-M = " +(N-M));
		System.out.println("Producte N*M = " +(N*M));
		System.out.println("Quocient N/M = " +(N/M));
		System.out.println("Resto N%M = " +(N%M));
		
		System.out.println("Doble de X = " +(2*X));
		System.out.println("Doble de Y = " +(2*Y));
		System.out.println("Doble de N = " +(2*N));
		System.out.println("Doble de M = " +(2*M));
		
		System.out.println("Suma de totes les variables = "+(X+Y+N+M));
		System.out.println("Producte de totes les variables = "+(X*Y*N*M));
		
	}

}
